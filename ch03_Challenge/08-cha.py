d = int(input('정수 입력 >>> '))
p = int(input('2의 지수승 입력 >>> '))
print()
print('정수', d, '<<', p, ',', '결과:', d << p)
print('정수', '67 * 2**3, 결과:', d * 2**3)
print('2진수(32비트): {0:32b}'.format(d), '정수:', d)
print('2진수(32비트): {0:32b}'.format(d << p), '정수: 67 << 3')
print('2진수(32비트): {0:32b}'.format(d * 2**p), '정수: 67 * 2**3')